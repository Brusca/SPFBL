CREATE TABLE `user_query` (
  `time` bigint(20) unsigned NOT NULL,
  `user` varchar(64) NOT NULL,
  `client` varchar(64) DEFAULT NULL,
  `ip` varchar(64) NOT NULL,
  `helo` varchar(128) DEFAULT NULL,
  `hostname` varchar(128) DEFAULT NULL,
  `sender` varchar(256) DEFAULT NULL,
  `qualifier` enum('PASS','FAIL','SOFTFAIL','NEUTRAL') DEFAULT NULL,
  `recipient` varchar(64) NOT NULL,
  `tokenSet` text NOT NULL,
  `complainKey` varchar(64) NOT NULL,
  `result` enum('WHITE','ACCEPT','GREYLIST','FLAG','HOLD','INEXISTENT','FAIL','REJECT','BLOCK','INVALID','NXDOMAIN') NOT NULL,
  `mailFrom` varchar(128) DEFAULT NULL,
  `replyto` varchar(128) DEFAULT NULL,
  `subject` text CHARACTER SET utf8mb4,
  `messageID` varchar(256) DEFAULT NULL,
  `unsubscribe` varchar(128) DEFAULT NULL,
  `linkMap` text,
  `malware` varchar(64) DEFAULT NULL,
  `adminAdvised` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `senderAdvised` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `recipientAdvised` tinyint(1) unsigned NOT NULL DEFAULT '0',
  PRIMARY KEY (`time`),
  KEY `id_user_query_messageID` (`messageID`),
  KEY `id_user_query_user` (`user`),
  KEY `id_user_query_result` (`result`) USING BTREE,
  KEY `id_user_query_adminAdvised` (`adminAdvised`),
  KEY `id_user_query_senderAdvised` (`senderAdvised`),
  KEY `id_user_query_recipientAdvised` (`recipientAdvised`),
  KEY `id_user_query_complainKey` (`complainKey`) USING BTREE
) ENGINE=MyISAM DEFAULT CHARSET=ascii;
